void	print_address(unsigned long int num, int index)
{
	const char	*zero = "0000000000000000";
	const char	*hex = "0123456789abcdef";
	char		c;

	if (num > 15)
		print_address(num / 16, index + 1);
	else
		write(1, zero, 15 - index);
	write(1, &hex[num % 16], 1);
	if (index == 0)
		write(1, ": ", 2);
}

void	print_char_hex(void *addr, char *cur_addr, int size)
{
	int		i;
	char	*hex = "0123456789abcdef";

	i = 0;
	while (i < 16)
	{
		if ((void *)cur_addr + i < addr + size)
		{
			write(1, &hex[cur_addr[i] / 16], 1);
			write(1, &hex[cur_addr[i] % 16], 1);
		}
		else
			write(1, "  ", 2);
		if (i % 2 == 1)
			write(1, " ", 1);
		i++;
	}
}

void	print_str(void *addr, char *cur_addr, int size)
{
	int	i;

	i = 0;
	while (i < 16 && ((void *)cur_addr + i < addr + size))
	{
		if (cur_addr[i] >= ' ' && cur_addr[i] < 127)
			write(1, &cur_addr[i], 1);
		else
			write(1, ".", 1);
		i++;
	}
	write(1, "\n", 1);
}

void	*ft_print_memory(void *addr, unsigned int size)
{
	int	i;
	char	*cur_addr;

	i = 0;
	cur_addr = (char *)addr;
	while ((void *)cur_addr < (addr + size))
	{
		print_address((unsigned long int)cur_addr, 0);
		print_char_hex(addr, cur_addr, size);
		print_str(addr, cur_addr, size);
		cur_addr += 16;
	}
	return (addr);
}

